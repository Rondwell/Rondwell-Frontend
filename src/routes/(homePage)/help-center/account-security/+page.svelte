	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Two-Factor Authentication (2FA)</h2>
		<p class="leading-relaxed text-gray-700">
			Enable 2FA from <strong>Settings → Security</strong> to add an extra layer of protection to your account. Rondwell supports authenticator apps (Google Authenticator, Authy) and SMS-based verification. Once enabled, you'll need your second factor whenever you log in from a new device.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">KYC Identity Verification</h2>
		<p class="leading-relaxed text-gray-700">
			To withdraw funds or access certain features, Rondwell requires identity verification (KYC). Go to <strong>Settings → Verification</strong> and upload a valid government-issued ID. Verification typically completes within 24 hours. Once verified, you'll see a badge on your profile.
		</p>
		<p class="leading-relaxed text-gray-700">
			Accepted documents include national ID cards, international passports, and driver's licenses.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Transaction PIN</h2>
		<p class="leading-relaxed text-gray-700">
			Your transaction PIN is required for all financial operations — withdrawals, transfers, and sensitive account changes. Set it up from <strong>Settings → Security</strong>. If you forget your PIN, use the "Reset PIN" option which sends a verification OTP to your registered email.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Managing Connected Devices</h2>
		<p class="leading-relaxed text-gray-700">
			View all devices currently logged into your account from the Security settings. You can remotely log out of any device if you notice unfamiliar activity. Rondwell also sends email notifications when your account is accessed from a new location.
		</p>
	</article>

