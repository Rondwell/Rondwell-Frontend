		<h2 class="text-2xl font-bold">Available Roles</h2>
		<ul class="ml-4 list-disc space-y-2 text-gray-700">
			<li><strong>Co-Organizer</strong> — full access to all event settings, tickets, and analytics</li>
			<li><strong>Registration Manager</strong> — manage attendees, check-ins, and ticket scanning</li>
			<li><strong>Support Manager</strong> — handle attendee queries and community moderation</li>
			<li><strong>Finance Manager</strong> — view revenue, refunds, and payout information</li>
		</ul>
		<p class="leading-relaxed text-gray-700">
			Each role has scoped permissions. You can revoke access or change roles at any time from the Team settings.
		</p>
	</article>

