		<h2 class="text-2xl font-bold">What Are Collections?</h2>
		<p class="leading-relaxed text-gray-700">
			Collections let you group multiple events under a single brand or theme — like a conference series, a monthly meetup, or a festival lineup. Each collection gets its own public page (<code class="rounded bg-gray-100 px-1 text-sm">rondwell.com/c/your-collection</code>) with a custom theme color, description, and social links.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Growing Subscribers</h2>
		<p class="leading-relaxed text-gray-700">
			Visitors to your collection page can subscribe to receive updates. Subscribers are notified whenever you add new events or send a newsletter blast. You can view subscriber count and engagement from the collection dashboard.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Sending Newsletters</h2>
		<p class="leading-relaxed text-gray-700">
			To send newsletters, your collection must first be <strong>verified</strong>. Submit a verification request from the collection settings page — Rondwell's team reviews it within 24–48 hours. Once approved, you can compose and send email campaigns to your subscriber base directly from the platform.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Customizing Your Collection Theme</h2>
		<p class="leading-relaxed text-gray-700">
			Give your collection its own identity from <strong>Settings → Display</strong>. Choose a colour palette, and Rondwell applies it across the collection's cover, cards, and buttons — and tints the mobile browser bar to match, so the page reads as a cohesive, branded destination. Add a profile picture, cover banner, and social links to complete the look.
		</p>
