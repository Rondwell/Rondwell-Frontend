		<h2 class="text-2xl font-bold">Creating Your Vendor Profile</h2>
		<p class="leading-relaxed text-gray-700">
			To become a vendor on Rondwell, go to <strong>Settings → Vendor Profile</strong> and enable your public profile. Fill in your business name, business type (catering, photography, décor, etc.), a short bio, and upload your logo and cover image.
		</p>
		<p class="leading-relaxed text-gray-700">
			Your public profile URL will be <code class="rounded bg-gray-100 px-1 text-sm">rondwell.com/v/your-slug</code>. You can customize the slug from the profile settings.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Listing Services & Products</h2>
		<p class="leading-relaxed text-gray-700">
			Add individual services or products to your profile with descriptions, pricing, and images. Organizers can browse your offerings and book directly through the platform. Bookings generate revenue that flows into your Rondwell wallet.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Getting Discovered</h2>
		<p class="leading-relaxed text-gray-700">
			Vendors appear on the <strong>Discover → Vendors</strong> page. To improve your visibility, complete your profile fully, add high-quality images, and keep your service listings up-to-date. Event organizers can search vendors by category, location, and service area.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Vendors, Exhibitors & Speakers</h2>
		<p class="leading-relaxed text-gray-700">
			Rondwell profiles aren't limited to vendors. You can also set up an <strong>Exhibitor</strong> profile (with a bookable booth) or a <strong>Speaker</strong> profile, each with its own public page — <code class="rounded bg-gray-100 px-1 text-sm">rondwell.com/v/…</code>, <code class="rounded bg-gray-100 px-1 text-sm">/exhibitor/…</code>, or <code class="rounded bg-gray-100 px-1 text-sm">/speaker/…</code>. Switch between your profiles from the account menu; each keeps its own listings, bookings, and analytics.
		</p>
