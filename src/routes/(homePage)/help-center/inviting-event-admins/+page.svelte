		<h2 class="text-2xl font-bold">How Invitations Work</h2>
		<p class="leading-relaxed text-gray-700">
			From your event's <strong>Settings → Team</strong> tab, enter a collaborator's email address, select their role, and send the invitation. They'll receive an email with a link to accept. Once accepted, they gain access to the relevant sections of your event based on their assigned role.
		</p>
