		<h2 class="text-2xl font-bold">Key Metrics Explained</h2>
		<ul class="ml-4 list-disc space-y-2 text-gray-700">
			<li><strong>Gross Revenue</strong> — total money collected before fees and refunds</li>
			<li><strong>Net Revenue</strong> — your actual earnings after platform fees</li>
			<li><strong>Conversion Rate</strong> — percentage of page visitors who registered/purchased</li>
			<li><strong>Check-in Rate</strong> — percentage of registered attendees who showed up</li>
			<li><strong>Traffic Sources</strong> — where your attendees are coming from (direct, social, referral)</li>
		</ul>
