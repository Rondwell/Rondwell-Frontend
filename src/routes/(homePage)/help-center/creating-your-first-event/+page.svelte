	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Getting Started</h2>
		<p class="leading-relaxed text-gray-700">
			To create an event on Rondwell, navigate to your dashboard and click the <strong>"Create Event"</strong> button. You can also use the AI-powered event creator by clicking "Create Event with AI" — just describe your event in a sentence and Rondwell will set up the details for you.
		</p>
		<p class="leading-relaxed text-gray-700">
			You'll be guided through a simple form where you provide:
		</p>
		<ul class="ml-4 list-disc space-y-2 text-gray-700">
			<li><strong>Event title</strong> — choose something descriptive and searchable</li>
			<li><strong>Date & time</strong> — set start and end times, timezone is auto-detected</li>
			<li><strong>Location</strong> — select Physical, Virtual, or Hybrid. For physical events, use the address picker</li>
			<li><strong>Event type</strong> — conference, meetup, workshop, concert, etc.</li>
			<li><strong>Cover image</strong> — recommended 1200×630px for best display on social shares</li>
		</ul>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Configuring Tickets</h2>
		<p class="leading-relaxed text-gray-700">
			After setting up basic details, head to the <strong>Tickets</strong> tab. You can create unlimited ticket types — free or paid. For paid tickets, set your price in NGN, USD, or EUR. Rondwell handles multi-currency conversion automatically.
		</p>
		<p class="leading-relaxed text-gray-700">
			Each ticket type can have its own capacity, sales window (start/end date), and visibility settings (public or hidden/invite-only).
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Multi-Day Events & Agenda</h2>
		<p class="leading-relaxed text-gray-700">
			If your event spans several days — like a conference or festival — toggle <strong>Multi-Day</strong> when setting the dates. Rondwell then lets you build a per-day <strong>Agenda</strong>: add sessions with their own titles, times, speakers, and rooms. Attendees see the agenda as a tabbed timeline on your public event page, and it stays in sync with the timezone you selected.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Event Themes & Branding</h2>
		<p class="leading-relaxed text-gray-700">
			Every event page can carry its own colour theme. Pick a palette from the event's <strong>Settings → Display</strong> tab and Rondwell applies it consistently across the cover, tabs, buttons — and even the mobile browser bar (the notification/status area tints to match), so your page feels like a branded, native app rather than a generic web page.
		</p>
		<p class="leading-relaxed text-gray-700">
			You can also set a custom link slug and a cover banner here to make your shareable link and social previews look polished.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Publishing Your Event</h2>
		<p class="leading-relaxed text-gray-700">
			Once you're happy with your event setup, click <strong>"Publish"</strong>. Your event gets a shareable link (e.g., <code class="rounded bg-gray-100 px-1 text-sm">rondwell.com/e/your-event-slug</code>) and appears on the Discover page for attendees to find.
		</p>
		<p class="leading-relaxed text-gray-700">
			You can customize your event page theme, add a custom slug, and configure SEO settings from the event's Settings tab.
		</p>
	</article>

