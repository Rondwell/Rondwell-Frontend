	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Understanding Your Wallet</h2>
		<p class="leading-relaxed text-gray-700">
			Every Rondwell account comes with two wallets: <strong>NGN</strong> and <strong>USD</strong>. Revenue from ticket sales, vendor bookings, and other transactions is automatically credited to the appropriate wallet based on the transaction currency.
		</p>
		<p class="leading-relaxed text-gray-700">
			Your wallet balance updates in real-time. Pending funds (from recent ticket sales) are held in escrow until the event completes or the settlement period passes.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Adding a Bank Account</h2>
		<p class="leading-relaxed text-gray-700">
			Navigate to <strong>Settings → Wallet</strong> and click "Add Bank Account." Enter your bank name, account number, and account holder name. Rondwell verifies the account instantly via Paystack. You can add multiple accounts and choose which one to withdraw to each time.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Making a Withdrawal</h2>
		<p class="leading-relaxed text-gray-700">
			From your wallet page, click <strong>"Withdraw"</strong>, select the destination bank account, enter the amount, and confirm with your transaction PIN. Withdrawals to Nigerian banks typically process within 24 hours. A small processing fee applies (see your plan's fee structure on the Pricing page).
		</p>
		<p class="leading-relaxed text-gray-700">
			USD withdrawals are processed via wire transfer and may take 2–5 business days depending on your bank.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Escrow & When Funds Become Available</h2>
		<p class="leading-relaxed text-gray-700">
			Ticket revenue doesn't hit your withdrawable balance instantly. Funds sit in a <strong>pending</strong> (escrow) state and become <strong>available</strong> once the payment is confirmed and the settlement window passes — typically after the event, or sooner depending on your account standing. Your wallet page always shows the split between pending and available balances so you know exactly what you can withdraw right now.
		</p>
	</article>

	<article class="my-8 flex flex-col gap-5">
		<h2 class="text-2xl font-bold">Verification & Withdrawal Requirements</h2>
		<p class="leading-relaxed text-gray-700">
			Because withdrawals move real money, you'll need a completed <strong>KYC verification</strong> and a <strong>transaction PIN</strong> before your first payout. First-time or unusually large withdrawals may trigger an additional review as part of Rondwell's fraud and compliance checks — this keeps your earnings and your attendees' payments safe.
		</p>
	</article>

